package assignments;

public class DoWhileLoop {
    public static void main(String[] args) {
        int i=5;
        do {
            System.out.println(i);
            i--;
       } while(i>=1);

        int j=1;
        do{
            System.out.println(j);
            j++;
        }while(j<=5);
    }
}

