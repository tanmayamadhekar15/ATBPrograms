package assignments;

public class evenForLoop {
    public static void main(String[] args) {
        for(int i=1;i<=30;i++){
            if(i%2==0){
                System.out.println("Even number :" +i);
            }
            else{
                System.out.println("Odd number:" +i);
            }
        }
    }
}
