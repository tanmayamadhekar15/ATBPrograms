package may18_encapsulationPoly.polymorphism.overloading;

public class constructorOverloading {

    constructorOverloading(){  // 0 parameter
    }

    constructorOverloading(int a){  //1 parameter

    }

    constructorOverloading(String b){ //1 parameter but different datatype

    }
    constructorOverloading(String c, String s){   //2 parameter

    }
    constructorOverloading(int c, String s){   //2 parameter but different datatype

    }
    constructorOverloading(String c, int s){   //2 parameter with order change

    }
}
