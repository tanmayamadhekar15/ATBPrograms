package pkgFirst;

public class Ex20 {
    public static void main(String[] args) {
        //Compound Assignments operators  += -= /= *=
        int a=10, b=45;
        b=b+30;
        System.out.println(b); // 45 + 30 = 75

        b+=30;
        System.out.println(b); // 75 + 30 = 105

        a-=10;
        System.out.println(a); // 10 - 10 = 0

        b/=5;
        System.out.println(b); // 105 / 5 = 21

        b*=a;
        System.out.println(a); // 21 * 0 = 0




    }
}
